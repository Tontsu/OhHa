public class Nappula {
    
    private final int y;
    private final int x;
    private int arvo;
    
    public Nappula(int j, int i, int arvo) {
        
        y = j;
        x = i; 
        arvo = this.arvo;
    }

    public int getY() {
        return y;
    }

    public int getX() {
        return x;
    }
    
}
