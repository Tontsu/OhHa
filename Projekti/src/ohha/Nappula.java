public class Nappula {
    
    private int y;
    private int x;
    private int arvo;
    
    public Nappula(int x, int y, int arvo) {
        
        y = this.y;
        x = this.x; 
        arvo = this.arvo;
    }

    public int getY() {
        return y;
    }

    public int getX() {
        return x;
    }
    
}
